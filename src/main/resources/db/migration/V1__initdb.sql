);

CREATE TABLE MEDICO (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY,
	nome varchar(255)
);

CREATE TABLE CONSULTA(
	id BIGINT GENERATED BY DEFAULT AS IDENTITY,
	id_medico BIGINT,
	id_paciente BIGINT,
	data date
);
