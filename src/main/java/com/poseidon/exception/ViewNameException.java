package com.poseidon.exception;

public class ViewNameException extends PoseidonException {

	public ViewNameException() {

	}

	public ViewNameException(String message) {
		super(message);

	}

	public ViewNameException(Throwable cause) {
		super(cause);

	}

	public ViewNameException(String message, Throwable cause) {
		super(message, cause);

	}

	public ViewNameException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
