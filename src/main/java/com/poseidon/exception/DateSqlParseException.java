package com.poseidon.exception;

public class DateSqlParseException extends PoseidonException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 2205794616246894466L;

	public DateSqlParseException() {
		super();

	}

	public DateSqlParseException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public DateSqlParseException(String message, Throwable cause) {
		super(message, cause);

	}

	public DateSqlParseException(String message) {
		super(message);

	}

	public DateSqlParseException(Throwable cause) {
		super(cause);

	}

	
	
}
