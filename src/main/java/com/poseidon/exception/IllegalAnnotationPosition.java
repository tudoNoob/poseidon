package com.poseidon.exception;

public class IllegalAnnotationPosition extends PoseidonException {

	public IllegalAnnotationPosition() {

	}

	public IllegalAnnotationPosition(String message) {
		super(message);

	}

	public IllegalAnnotationPosition(Throwable cause) {
		super(cause);

	}

	public IllegalAnnotationPosition(String message, Throwable cause) {
		super(message, cause);

	}

	public IllegalAnnotationPosition(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
