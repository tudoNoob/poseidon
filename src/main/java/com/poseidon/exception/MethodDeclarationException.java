package com.poseidon.exception;

public class MethodDeclarationException extends PoseidonException {

	public MethodDeclarationException() {

	}

	public MethodDeclarationException(String message) {
		super(message);

	}

	public MethodDeclarationException(Throwable cause) {
		super(cause);

	}

	public MethodDeclarationException(String message, Throwable cause) {
		super(message, cause);

	}

	public MethodDeclarationException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
